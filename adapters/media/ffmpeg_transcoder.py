    # MediaTranscoderPort
    # ------------------------------------------------------------------

    def ensure_hls_session(
        self,
        *,
        session_id: str,
        source_path: str,
        output_dir: str,
        audio_track: int | None = None,
        subtitle_track: int | None = None,
        start_segment_index: int = 0,
        segment_seconds: int | None = None,
        duration_seconds: float | None = None,
    ) -> dict[str, str]:
        _ = subtitle_track  # sidecar-only: subtitles are not burned into HLS video
        ffmpeg_cmd = shutil.which(self._ffmpeg_bin) or self._ffmpeg_bin
        seg_secs = (
            int(segment_seconds)
            if segment_seconds is not None
            else self._segment_seconds
        )
        seg_secs = max(2, seg_secs)
        start_index = max(0, int(start_segment_index))
        probed_duration = 0.0
        if duration_seconds is not None:
            try:
                probed_duration = max(0.0, float(duration_seconds))
            except (TypeError, ValueError):
                probed_duration = 0.0
        if probed_duration > 0:
            max_start = max(0, math.ceil(probed_duration / seg_secs) - 1)
            start_index = min(start_index, max_start)
        manifest_path = str(Path(output_dir) / "index.m3u8")
        ffmpeg_playlist = str(Path(output_dir) / self._FFMPEG_PLAYLIST_NAME)
        # When the application layer has pre-written ``index.m3u8`` we
        # must not let ffmpeg touch it — its hls muxer would clobber
        # the carefully-crafted full segment list. We also need to
        # avoid the canonical playlist whenever we're restarting mid-
        # stream (``start_index > 0``). Everything else (the original
        # "live" code path where nobody pre-wrote a manifest) keeps
        # the legacy behaviour: ffmpeg owns ``index.m3u8`` directly.
        caller_owns_playlist = os.path.isfile(manifest_path) or start_index > 0
        ffmpeg_target_playlist = (
            ffmpeg_playlist if caller_owns_playlist else manifest_path
        )

        with self._lock:
            self._reap_finished_locked()
            existing = self._active.get(session_id)
            reuse = SessionReusePolicy(
                process_alive=(
                    existing is not None and existing.process.poll() is None
                ),
                same_start_index=(
                    existing is not None
                    and existing.start_segment_index == start_index
                ),
                same_segment_seconds=(
                    existing is not None and existing.segment_seconds == seg_secs
                ),
                same_audio_track=(
                    existing is not None and existing.audio_track == audio_track
                ),
                same_source_path=(
                    existing is not None and existing.source_path == source_path
                ),
            )
            if existing is not None and reuse.allows_reuse():
                return {
                    "manifest_path": existing.manifest_path,
                    "output_dir": existing.output_dir,
                    "start_segment_index": str(existing.start_segment_index),
                    "segment_seconds": str(existing.segment_seconds),
                }
            # Tear down a divergent process before launching a new one
            # so that two ffmpeg instances don't fight over the same
            # output directory. This is what makes "seek beyond the
            # current encode head" possible.
            purge_segments = False
            if existing is not None:
                # A later encode that started further into the file leaves
                # ``segment_NNNNN.ts`` files whose timestamps align with that
                # offset. If we restart ffmpeg *earlier* (common when the
                # player prefetches segment 0 while the session was started
                # mid-file from a resume hint), those high-index segments
                # become stale: their PTS no longer matches the new low
                # segments and Shaka aborts with a generic decode error.
                # Different audio or segment length also invalidates every
                # existing transport stream.
                if (
                    start_index < existing.start_segment_index
                    or audio_track != existing.audio_track
                    or seg_secs != existing.segment_seconds
                ):
                    purge_segments = True
                self._terminate(existing.process)
                self._active.pop(session_id, None)
            while len(self._active) >= self._max_active_sessions:
                if not self._evict_oldest_locked(excluding=session_id):
                    raise InfrastructureError(
                        "Media server is busy: too many active transcoding sessions."
                    )

            os.makedirs(output_dir, exist_ok=True)
            if purge_segments:
                self._purge_ts_segments(output_dir)
            # The HLS muxer's ``append_list`` flag refuses to truncate
            # an existing playlist file, which on a seek-on-demand
            # restart leaves behind stale entries from the previous
            # encode. Removing the sentinel up front gives ffmpeg a
            # clean slate and avoids the muxer hanging while trying
            # to merge incompatible playlist states.
            self._clear_stale_ffmpeg_artifacts(
                ffmpeg_playlist=ffmpeg_playlist,
                output_dir=output_dir,
            )
            log_path = str(Path(output_dir) / self._FFMPEG_LOG_NAME)
            command = self._build_command(
                ffmpeg_cmd=ffmpeg_cmd,
                source_path=source_path,
                playlist_output=ffmpeg_target_playlist,
                output_dir=output_dir,
                audio_track=audio_track,
                start_segment_index=start_index,
                segment_seconds=seg_secs,
                duration_seconds=probed_duration,
            )
            _LOG.info(
                "ffmpeg_spawn source=%s start_seg=%s audio=%s cmd=%s",
                source_path,
                start_index,
                audio_track,
                " ".join(command),
            )
            self._write_spawn_record(log_path, command)
            process = self._spawn_ffmpeg(command, log_path)
            self._active[session_id] = _ActiveTranscode(
                session_id=session_id,
                output_dir=output_dir,
                manifest_path=manifest_path,
                process=process,
                started_at=time.time(),
                start_segment_index=start_index,
                segment_seconds=seg_secs,
                source_path=source_path,
                audio_track=audio_track,
            )
            _LOG.info(
                "transcode_started session=%s active=%s source=%s start_seg=%s log=%s",
                session_id,
                len(self._active),
                source_path,
                start_index,
                log_path,
            )

        # If the application layer pre-wrote ``index.m3u8`` we can
        # return immediately — the client only needs the manifest,
        # not the first segment. Otherwise, we keep the legacy
        # behaviour of blocking until ffmpeg has produced ``index.m3u8``
        # so the client has something to load.
        if not os.path.isfile(manifest_path):
            self._wait_for_initial_manifest(
                session_id=session_id,
                process=process,
                manifest_path=manifest_path,
            )

        self._sync_active_gauge()
        return {
            "manifest_path": manifest_path,
            "output_dir": output_dir,
            "start_segment_index": str(start_index),
            "segment_seconds": str(seg_secs),
            "log_path": log_path,
        }

    def stop_hls_session(self, session_id: str) -> None:
        with self._lock:
            active = self._active.pop(session_id, None)
        if active is None:
            return
        self._terminate(active.process)
        self._sync_active_gauge()
        _LOG.info("transcode_stopped session=%s", session_id)

    def is_hls_session_running(self, session_id: str) -> bool:
        with self._lock:
            self._reap_finished_locked()
            active = self._active.get(session_id)
            return active is not None and active.process.poll() is None

    @staticmethod
    def _stat_signature(source_path: str) -> tuple[int, int] | None:
        """File identity for probe caching; ``None`` when the file is gone."""
        try:
            stat = os.stat(source_path)
        except OSError:
            return None
        return (stat.st_mtime_ns, stat.st_size)

    @staticmethod
    def _copy_tracks(
        tracks: dict[str, list[dict[str, object]]],
    ) -> dict[str, list[dict[str, object]]]:
        return {key: [dict(item) for item in items] for key, items in tracks.items()}

    def probe_media_tracks(self, source_path: str) -> dict[str, list[dict[str, object]]]:
        signature = self._stat_signature(source_path)
        if signature is not None:
            with self._probe_cache_lock:
                cached = self._tracks_cache.get(source_path)
                if cached is not None and cached[0] == signature:
                    return self._copy_tracks(cached[1])
        tracks = self._probe_media_tracks_uncached(source_path)
        if tracks is None:
            return {"audio": [], "subtitles": []}
        if signature is not None:
            with self._probe_cache_lock:
                self._tracks_cache[source_path] = (signature, self._copy_tracks(tracks))
        return tracks

    def _probe_media_tracks_uncached(
        self, source_path: str
    ) -> dict[str, list[dict[str, object]]] | None:
        """Run ffprobe for stream metadata; ``None`` when the probe fails."""
        ffprobe_cmd = shutil.which(self._ffprobe_bin) or self._ffprobe_bin
        command = [
            ffprobe_cmd,
            "-v",
            "error",
            "-show_entries",
            "stream=index,codec_type,codec_name:stream_tags=language,title",
            "-of",
            "json",
            source_path,
        ]
        try:
            result = subprocess.run(
                command,
                check=True,
                capture_output=True,
                text=True,
                encoding="utf-8",
                errors="replace",
                timeout=15,
            )
            payload = json.loads(result.stdout or "{}")
            streams = list(payload.get("streams") or [])
        except Exception:
            return None

        audio: list[dict[str, object]] = []
        subtitles: list[dict[str, object]] = []
        audio_pos = 0
        sub_pos = 0
        for stream in streams:
            kind = str(stream.get("codec_type") or "")
            tags = stream.get("tags") or {}
            lang = str(tags.get("language") or "und")
            title = str(tags.get("title") or "").strip()
            label = f"{lang.upper()}"
            if title:
                label = f"{label} · {title}"
            if kind == "audio":
                audio.append({"id": audio_pos, "label": label})
                audio_pos += 1
            elif kind == "subtitle":
                codec_raw = str(stream.get("codec_name") or "").strip().lower()
                subtitles.append({"id": sub_pos, "label": label, "codec": codec_raw})
                sub_pos += 1
        return {"audio": audio, "subtitles": subtitles}

    def probe_media_duration(self, source_path: str) -> float:
        """Return the duration of ``source_path`` in seconds.

        We try the container-level ``format=duration`` first, then fall
        back to the longest stream duration. Returns ``0.0`` when the
        duration is not available — the caller is expected to drop back
        to a live-style playlist when that happens.
        """
        signature = self._stat_signature(source_path)
        if signature is not None:
            with self._probe_cache_lock:
                cached = self._duration_cache.get(source_path)
                if cached is not None and cached[0] == signature:
                    return cached[1]
        duration = self._probe_media_duration_uncached(source_path)
        # A zero duration usually means the probe failed or the file is
        # still being written; don't pin that answer in the cache.
        if duration > 0 and signature is not None:
            with self._probe_cache_lock:
                self._duration_cache[source_path] = (signature, duration)
        return duration

    def _probe_media_duration_uncached(self, source_path: str) -> float:
        ffprobe_cmd = shutil.which(self._ffprobe_bin) or self._ffprobe_bin
        command = [
            ffprobe_cmd,
            "-v",
            "error",
            "-show_entries",
            "format=duration:stream=duration",
            "-of",
            "json",
            source_path,
        ]
        try:
            result = subprocess.run(
                command,
                check=True,
                capture_output=True,
                text=True,
                encoding="utf-8",
                errors="replace",
                timeout=15,
            )
            payload = json.loads(result.stdout or "{}")
        except Exception:
            return 0.0

        def _as_float(value: object) -> float:
            try:
                f = float(value)  # type: ignore[arg-type]
            except (TypeError, ValueError):
                return 0.0
            if f != f:  # NaN guard
                return 0.0
            return max(0.0, f)

        fmt_dur = _as_float((payload.get("format") or {}).get("duration"))
        if fmt_dur > 0:
            return fmt_dur
        stream_durations = [
            _as_float(stream.get("duration"))
            for stream in (payload.get("streams") or [])
        ]
        return max(stream_durations, default=0.0)

