"""FFmpeg-backed HLS transcoder adapter.

The adapter spins up one ffmpeg process per playback session. Encoding
runs in the background and writes MPEG-TS segments named
``segment_NNNNN.ts`` to the session's output directory. The HLS
playlist that the *client* consumes (``index.m3u8``) is pre-written by
the application layer once the source's total duration is known — we
deliberately have ffmpeg write its own playlist to a sentinel file
(``_ffmpeg.m3u8``) and ignore it so we can support arbitrary seeking
without the playlist growing piecewise.

To make user seeks land instantly, the application layer can call
:meth:`ensure_hls_session` again with ``start_segment_index=N``. We
then terminate the current ffmpeg process and spawn a new one that
input-seeks to ``N * segment_seconds`` and resumes segment numbering at
``N``. ``-output_ts_offset`` stamps muxed packets on the canonical
playlist clock so the player can splice from any previous segment to
the new ones without an ``#EXT-X-DISCONTINUITY`` marker. Seek restarts
must not use ``-avoid_negative_ts make_zero`` (it cancels the offset)
and hardware encoders need forced-IDR mode so ``-force_key_frames``
actually cuts on the playlist cadence.
"""

from __future__ import annotations

import json
import logging
import math
import os
import shutil
import subprocess
import threading
import time
from dataclasses import dataclass
from pathlib import Path
from typing import Callable

from adapters.media.ffmpeg_encoder import build_video_encode_args, resolve_video_encoder
from domain.errors import InfrastructureError
from shared.telemetry import get_telemetry

_LOG = logging.getLogger(__name__)

# Sidecar subtitles only — these image-based codecs cannot be extracted to WebVTT.
IMAGE_SUBTITLE_CODECS: frozenset[str] = frozenset(
    {
        "hdmv_pgs_subtitle",
        "pgssub",
        "dvd_subtitle",
        "dvb_subtitle",
        "xsub",
        "mov_text",
    }
)


@dataclass(frozen=True, slots=True)
class SessionReusePolicy:
    """When an existing ffmpeg process may be reused vs respawned."""

    same_start_index: bool
    same_segment_seconds: bool
    same_audio_track: bool
    same_source_path: bool
    process_alive: bool

    def allows_reuse(self) -> bool:
        return all(
            (
                self.process_alive,
                self.same_start_index,
                self.same_segment_seconds,
                self.same_audio_track,
                self.same_source_path,
            )
        )


@dataclass(slots=True)
class _ActiveTranscode:
    session_id: str
    output_dir: str
    manifest_path: str
    process: subprocess.Popen[bytes]
    started_at: float
    start_segment_index: int
    segment_seconds: int
    source_path: str
    audio_track: int | None


class FFmpegTranscoderAdapter:
    """Materialize HLS artifacts using one ffmpeg process per session."""

    # ffmpeg writes its own playlist to this name inside the session
    # directory. We never serve this file; the canonical playlist that
    # the client receives (``index.m3u8``) is generated by the
    # application layer once the source duration is known.
    _FFMPEG_PLAYLIST_NAME = "_ffmpeg.m3u8"
    # Per-session ffmpeg stderr is mirrored here so a failed transcode
    # leaves behind a forensic trail without polluting the process
    # stdout. The file is opened in append mode; restarts share it.
    _FFMPEG_LOG_NAME = "_ffmpeg.log"

    def __init__(
        self,
        *,
        ffmpeg_bin: str = "ffmpeg",
        ffprobe_bin: str = "ffprobe",
        max_active_sessions: int = 2,
        segment_seconds: int = 4,
        startup_timeout_seconds: int = 15,
        video_encoder: str = "auto",
    ) -> None:
        self._ffmpeg_bin = ffmpeg_bin
        self._ffprobe_bin = ffprobe_bin
        self._max_active_sessions = max(1, int(max_active_sessions))
        self._segment_seconds = max(2, int(segment_seconds))
        self._startup_timeout_seconds = max(3, int(startup_timeout_seconds))
        self._video_encoder = resolve_video_encoder(
            requested=video_encoder,
            ffmpeg_bin=ffmpeg_bin,
        )
        self._active: dict[str, _ActiveTranscode] = {}
        self._lock = threading.RLock()
        # ffprobe results keyed by path, validated against (mtime_ns, size)
        # so repeated episode listings don't re-spawn ffprobe per file.
        self._probe_cache_lock = threading.Lock()
        self._tracks_cache: dict[str, tuple[tuple[int, int], dict[str, list[dict[str, object]]]]] = {}
        self._duration_cache: dict[str, tuple[tuple[int, int], float]] = {}
        self._telemetry = get_telemetry()
        self._activity_rank: Callable[[str], float] | None = None
        self._on_evicted: Callable[[str], None] | None = None
        _LOG.info("ffmpeg_transcoder_init video_encoder=%s", self._video_encoder)

    def set_session_hooks(
        self,
        *,
        activity_rank: Callable[[str], float] | None = None,
        on_evicted: Callable[[str], None] | None = None,
    ) -> None:
        """Optional hooks from :class:`PlaybackService` for eviction policy."""
        self._activity_rank = activity_rank
        self._on_evicted = on_evicted

    def _sync_active_gauge(self, *, _lock_held: bool = False) -> None:
        if _lock_held:
            count = len(self._active)
        else:
            with self._lock:
                count = len(self._active)
        self._telemetry.set_gauge("ffmpeg.active_sessions", float(count))

