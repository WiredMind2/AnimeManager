    # Implementation details
    # ------------------------------------------------------------------

    def _reap_finished_locked(self) -> None:
        removed = False
        for session_id, active in list(self._active.items()):
            if active.process.poll() is not None:
                code = active.process.returncode
                _LOG.warning(
                    "transcode_exited session=%s code=%s start_seg=%s source=%s",
                    session_id,
                    code,
                    active.start_segment_index,
                    active.source_path,
                )
                self._log_exit_footer(active.output_dir, code)
                self._active.pop(session_id, None)
                removed = True
        if removed:
            self._sync_active_gauge(_lock_held=True)

    def _evict_oldest_locked(self, *, excluding: str) -> bool:
        """Stop the least-active session to make room for a new encode."""
        candidates = [
            (sid, active)
            for sid, active in self._active.items()
            if sid != excluding
        ]
        if not candidates:
            return False

        def _rank(item: tuple[str, _ActiveTranscode]) -> tuple[float, float]:
            sid, active = item
            if self._activity_rank is not None:
                activity = self._activity_rank(sid)
                if activity > 0:
                    return (activity, active.started_at)
            return (0.0, active.started_at)

        victim_id, victim = min(candidates, key=_rank)
        _LOG.warning(
            "transcode_evicted victim=%s for incoming=%s active=%s",
            victim_id,
            excluding,
            len(self._active),
        )
        self._terminate(victim.process)
        self._active.pop(victim_id, None)
        self._sync_active_gauge(_lock_held=True)
        if self._on_evicted is not None:
            try:
                self._on_evicted(victim_id)
            except Exception as exc:  # noqa: BLE001
                _LOG.warning(
                    "transcode_eviction_callback_failed victim=%s err=%s",
                    victim_id,
                    exc,
                )
        return True

    @staticmethod
    def _log_exit_footer(output_dir: str, exit_code: int | None) -> None:
        log_path = Path(output_dir) / FFmpegTranscoderAdapter._FFMPEG_LOG_NAME
        try:
            with open(log_path, "ab") as fh:
                fh.write(
                    f"\n[AnimeManager] ffmpeg exited with code {exit_code}\n".encode(
                        "utf-8",
                        errors="replace",
                    )
                )
        except OSError:
            pass

    def _build_command(
        self,
        *,
        ffmpeg_cmd: str,
        source_path: str,
        playlist_output: str,
        output_dir: str,
        audio_track: int | None,
        start_segment_index: int,
        segment_seconds: int,
        duration_seconds: float = 0.0,
    ) -> list[str]:
        segment_pattern = str(Path(output_dir) / "segment_%05d.ts")
        # Input-seek to N*seg_secs so that ffmpeg's first emitted segment
        # corresponds to the requested playlist position. ``-output_ts_offset``
        # stamps muxed MPEG-TS packets on the canonical playlist clock
        # without ``-copyts``, which preserved raw source timestamps and
        # made browsers report UINT32-scale ``video.duration`` values.
        #
        # Critical: do **not** combine ``-avoid_negative_ts make_zero`` with
        # ``-output_ts_offset`` on seek restarts. ``make_zero`` rebases the
        # packet clock so the offset never lands in the .ts files — seek-ahead
        # segments then start near PTS 0 while earlier segments / the playlist
        # stay on the absolute timeline, and Shaka plays A/V off the scrubber.
        # Encoder time ``t`` is still 0-based after input ``-ss``, so forced
        # keyframes use ``n_forced*seg`` (not ``seek+n_forced*seg``); the
        # absolute form delays IDRs and yields irregular segment lengths that
        # disagree with ``#EXTINF``.
        seek_seconds = start_segment_index * segment_seconds
        if duration_seconds > 0:
            # Avoid landing past EOF when ffprobe over-estimates duration.
            seek_seconds = min(seek_seconds, max(0.0, duration_seconds - segment_seconds))
        command: list[str] = [
            ffmpeg_cmd,
            "-hide_banner",
            "-loglevel",
            "error",
            "-nostdin",
            "-y",
            "-fflags",
            "+genpts",
        ]
        if seek_seconds > 0:
            command.extend(["-ss", f"{seek_seconds}"])
        command.extend(["-i", source_path])
        if seek_seconds > 0:
            command.extend(["-output_ts_offset", f"{seek_seconds:g}"])
        else:
            command.extend(["-avoid_negative_ts", "make_zero"])
        keyframe_expr = f"expr:gte(t,n_forced*{segment_seconds})"
        command.extend(["-map", "0:v:0"])
        if audio_track is None:
            command.extend(["-map", "0:a:0?"])
        else:
            command.extend(["-map", f"0:a:{max(0, int(audio_track))}?"])
        # ``-force_key_frames`` guarantees a key-frame at every segment
        # boundary so the resulting .ts files are independently
        # decodable, which HLS requires for arbitrary seeking.
        video_args = build_video_encode_args(
            self._video_encoder,
            keyframe_expr=keyframe_expr,
        )
        output_args: list[str] = [
                *video_args,
                "-c:a",
                "aac",
                "-ar",
                "48000",
                "-ac",
                "2",
                "-b:a",
                "160k",
                "-max_muxing_queue_size",
                "1024",
                "-f",
                "hls",
                "-hls_time",
                str(segment_seconds),
                "-hls_playlist_type",
                "event",
                "-hls_list_size",
                "0",
                # ``append_list`` would tell ffmpeg to keep merging
                # into any existing playlist file, which conflicts
                # with us recycling the same output directory across
                # seek-on-demand restarts. ``temp_file`` makes ffmpeg
                # write to ``segment_NNNNN.ts.tmp`` and rename on
                # completion so we never observe a partial segment.
                "-hls_flags",
                "independent_segments+temp_file",
                "-hls_segment_type",
                "mpegts",
                "-hls_segment_filename",
                segment_pattern,
                "-start_number",
                str(start_segment_index),
        ]
        if seek_seconds <= 0:
            output_args.extend(["-reset_timestamps", "1"])
        output_args.append(playlist_output)
        command.extend(output_args)
        return command

    def _wait_for_initial_manifest(
        self,
        *,
        session_id: str,
        process: subprocess.Popen[bytes],
        manifest_path: str,
    ) -> None:
        """Legacy startup path used when the caller did not pre-write
        a VOD playlist. We block until ffmpeg produces ``index.m3u8``
        so the client has something to load. The ``_spawn_with_…``
        helper has already verified that the process is alive past
        its early-death window, so here we only need to wait."""
        deadline = time.time() + self._startup_timeout_seconds
        while time.time() < deadline:
            if os.path.isfile(manifest_path):
                return
            if process.poll() is not None:
                with self._lock:
                    self._active.pop(session_id, None)
                self._sync_active_gauge()
                _LOG.warning("transcode_failed_early session=%s", session_id)
                raise InfrastructureError(
                    "Transcoder exited before playlist became ready."
                )
            time.sleep(0.2)

    def materialize_subtitle_tracks(
        self,
        *,
        source_path: str,
        output_dir: str,
    ) -> list[dict[str, object]]:
        """Extract text subtitle tracks as WebVTT sidecars.

        For SSA/ASS (``codec_name`` of ``ass`` or ``ssa``), also writes a
        ``.ass`` sidecar (stream copy) for high-fidelity browser rendering
        via libass (see web player).

        Returned entries have ``id``/``label``/``codec`` matching
        ``probe_media_tracks`` plus ``filename`` (WebVTT) and optional
        ``ass_filename`` for files written under ``output_dir``.
        """
        ffmpeg_cmd = shutil.which(self._ffmpeg_bin) or self._ffmpeg_bin
        tracks = self.probe_media_tracks(source_path).get("subtitles", [])
        out: list[dict[str, object]] = []
        for track in tracks:
            try:
                sub_id = max(0, int(track.get("id")))  # type: ignore[arg-type]
            except (TypeError, ValueError):
                continue
            codec = str(track.get("codec") or "").strip().lower()
            label = str(track.get("label") or f"Subtitle {sub_id}")
            if codec in IMAGE_SUBTITLE_CODECS:
                _LOG.warning(
                    "subtitle_sidecar_unsupported codec=%s track=%s path=%s",
                    codec,
                    sub_id,
                    source_path,
                )
                out.append(
                    {
                        "id": sub_id,
                        "label": label,
                        "codec": codec,
                        "error": "image_subtitles_not_supported",
                    }
                )
                continue
            filename = f"subtitle_{sub_id:03d}.vtt"
            target = str(Path(output_dir) / filename)
            command = [
                ffmpeg_cmd,
                "-hide_banner",
                "-loglevel",
                "error",
                "-nostdin",
                "-y",
                "-i",
                source_path,
                "-map",
                f"0:s:{sub_id}?",
                "-f",
                "webvtt",
                target,
            ]
            try:
                subprocess.run(
                    command,
                    check=True,
                    stdout=subprocess.DEVNULL,
                    stderr=subprocess.DEVNULL,
                    timeout=30,
                )
            except Exception as exc:
                _LOG.warning(
                    "subtitle_vtt_extract_failed track=%s codec=%s path=%s err=%s",
                    sub_id,
                    codec,
                    source_path,
                    exc,
                )
                out.append(
                    {
                        "id": sub_id,
                        "label": label,
                        "codec": codec,
                        "error": "subtitle_extract_failed",
                    }
                )
                continue
            if not os.path.isfile(target):
                out.append(
                    {
                        "id": sub_id,
                        "label": label,
                        "codec": codec,
                        "error": "subtitle_extract_empty",
                    }
                )
                continue
            row: dict[str, object] = {
                "id": sub_id,
                "label": label,
                "filename": filename,
                "codec": codec,
            }
            if codec in ("ass", "ssa"):
                ass_name = f"subtitle_{sub_id:03d}.ass"
                ass_target = str(Path(output_dir) / ass_name)
                ass_cmd = [
                    ffmpeg_cmd,
                    "-hide_banner",
                    "-loglevel",
                    "error",
                    "-nostdin",
                    "-y",
                    "-i",
                    source_path,
                    "-map",
                    f"0:s:{sub_id}?",
                    "-c",
                    "copy",
                    ass_target,
                ]
                try:
                    subprocess.run(
                        ass_cmd,
                        check=True,
                        stdout=subprocess.DEVNULL,
                        stderr=subprocess.DEVNULL,
                        timeout=30,
                    )
                except Exception:
                    pass
                if os.path.isfile(ass_target) and os.path.getsize(ass_target) > 0:
                    row["ass_filename"] = ass_name
            out.append(row)
        return out

    @staticmethod
    def _write_spawn_record(log_path: str, command: list[str]) -> None:
        try:
            with open(log_path, "ab") as fh:
                stamp = time.strftime("%Y-%m-%d %H:%M:%S")
                fh.write(
                    f"\n[AnimeManager] ffmpeg spawn {stamp}\n{' '.join(command)}\n".encode(
                        "utf-8",
                        errors="replace",
                    )
                )
        except OSError:
            pass

    def _spawn_ffmpeg(
        self,
        command: list[str],
        log_path: str,
    ) -> subprocess.Popen[bytes]:
        # Mirror stderr to a per-session log file so failures (subtitle
        # filter errors, unreadable input, codec issues) leave a
        # forensic trail under the session's output directory. We open
        # in append-binary mode and immediately close our handle —
        # ``subprocess.Popen`` keeps its own dup of the file descriptor
        # for the lifetime of the child.
        log_file = open(log_path, "ab")
        try:
            return subprocess.Popen(
                command,
                stdout=subprocess.DEVNULL,
                stderr=log_file,
            )
        finally:
            log_file.close()

    @staticmethod
    def _purge_ts_segments(output_dir: str) -> None:
        """Remove completed ``segment_*.ts`` files before an incompatible
        ffmpeg restart. See :meth:`ensure_hls_session` for when this runs."""
        try:
            for entry in os.listdir(output_dir):
                if not entry.startswith("segment_") or not entry.endswith(".ts"):
                    continue
                try:
                    os.remove(str(Path(output_dir) / entry))
                except OSError:
                    pass
        except FileNotFoundError:
            pass

    @staticmethod
    def _clear_stale_ffmpeg_artifacts(*, ffmpeg_playlist: str, output_dir: str) -> None:
        """Remove leftovers from a previous ffmpeg run so the new
        process starts from a known-clean state. We deliberately do
        *not* touch ``index.m3u8`` (that's the canonical playlist the
        application layer owns) or the produced ``segment_NNNNN.ts``
        files (those remain valid across restarts when
        ``-output_ts_offset`` keeps each encode on the playlist clock)."""
        try:
            os.remove(ffmpeg_playlist)
        except FileNotFoundError:
            pass
        except OSError as exc:
            _LOG.debug("ffmpeg_playlist_unlink_failed path=%s err=%s", ffmpeg_playlist, exc)
        # Stray ``segment_NNNNN.ts.tmp`` files from a hard kill of a
        # previous process can confuse the new ffmpeg's atomic rename.
        try:
            for entry in os.listdir(output_dir):
                if entry.endswith(".ts.tmp"):
                    try:
                        os.remove(str(Path(output_dir) / entry))
                    except OSError:
                        pass
        except FileNotFoundError:
            pass

    def _terminate(self, process: subprocess.Popen[bytes]) -> None:
        if process.poll() is not None:
            return
        try:
            process.terminate()
            process.wait(timeout=3)
        except Exception:
            try:
                process.kill()
                process.wait(timeout=1)
            except Exception:
                pass


